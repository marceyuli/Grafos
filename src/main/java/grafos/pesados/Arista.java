package grafos.pesados;


public class Arista {
    int verticeOrigen;
    int verticeDestino;
    double peso;

    public Arista(int verticeOrigen, int verticeDestino, double peso) {
        this.verticeOrigen = verticeOrigen;
        this.verticeDestino = verticeDestino;
        this.peso = peso;
    }
    
}
